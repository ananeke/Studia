a = float(input("Podaj pierwszą liczbę: "))
b = float(input("Pojdaj drugą liczbę: "))

print("Oto suma tych dwóch liczb", a+b)
print("Oto róznica tych dwóch liczb", a-b)
print("Oto iloczyn tych liczb", a*b)
if b == 0.0:
    print("Nie wolno dzielić przez 0!")
else:
    print("Oto ilozraz tych dwóch liczb", a/b)
print("To koniec")
